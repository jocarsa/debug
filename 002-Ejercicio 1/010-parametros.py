
def calcula(operando1,operando2,operador):
    if operador == "+":
        return operando1+operando2
    elif operador == "-":
        return operando1+operando2
    elif operador == "*":
        return operando1*operando2
    elif operador == "/":
        return operando1/operando2

print(calcula(4,3,"+"))




